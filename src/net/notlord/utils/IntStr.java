package net.notlord.utils;

public class IntStr {
	public String string;
	public int integer;

	public IntStr() {}

	public IntStr(String string) {
		this.string = string;
	}

	public IntStr(int integer) {
		this.integer = integer;
	}

	public IntStr(String string, int integer) {
		this.string = string;
		this.integer = integer;
	}
}
